#include <iostream>

int main(){
    std::cout << "Operations in C++" << '\n';
    std::cout << "---------------------------" << '\n';

//  To add in C++ use '+' operator
    std::cout <<"1+2 = " << 1+2 << '\n'; 

// Subtract '-'
    std::cout <<"3-1 = " << 3-1 << '\n';

//  Multiply '*'
    std::cout <<"2x2 = " << 2*2 << '\n';

//  Divison '/'
    std::cout <<"10 divided by 2 = " << 10/2 << '\n';
    return 0;



}